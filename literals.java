public class literals {
    public static void main(String[] args) {
        byte age = 34;
        char ch = 'A';
        double db = 3.344D;
        float fl = 2.3f;
        boolean q = true;
        String s = "farish";
        System.out.println(age);
        System.out.println(ch);
        System.out.println(db);
        System.out.println(fl);
        System.out.println(q);
        System.out.println(s);
    }
}
